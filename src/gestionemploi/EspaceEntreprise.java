    public ImageIcon ResizeImage(String cheminImage, byte[] photo) {
        ImageIcon monImage = null;

        if (cheminImage != null) {
            monImage = new ImageIcon(cheminImage);
        } else {
            monImage = new ImageIcon(photo);
        }

        Image img = monImage.getImage();
        Image img2 = img.getScaledInstance(label_photo.getWidth(), label_photo.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(img2);
        return image;
    }

    public ArrayList<Demandeur> getListDemandeur() {
        ArrayList<Demandeur> listDemandeur = new ArrayList<Demandeur>();
        Connection con = Connexion.BaseDeDonnee();
        Statement stat = null;
        ResultSet res = null;
        String sql = "SELECT * FROM demandeur";
        try {
            stat = con.createStatement();
            res = stat.executeQuery(sql);
            Demandeur demandeur;
            while (res.next()) {
                demandeur = new Demandeur(res.getInt("identifiant"), res.getBytes("photo"), res.getString("nomDemandeur"), res.getString("prenomDemandeur"),
                        res.getString("sexe"), res.getString("dateDeNaissance"), res.getString("nationalite"), res.getString("telephone"),
                        res.getString("adresse"), res.getString("diplome"), res.getString("anneeObtention"), res.getString("domaine"),
                        res.getString("statut"), res.getString("cv"), res.getString("emailDemandeur"));
                
                listDemandeur.add(demandeur);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        return listDemandeur;
    }
