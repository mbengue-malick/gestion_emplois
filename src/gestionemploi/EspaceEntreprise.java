    public void Afficher_Table_Demandeur() {

        ArrayList<Demandeur> list = getListDemandeur();
        DefaultTableModel model = (DefaultTableModel) table_demandeur.getModel();

//Effacer le contenu du JTable
        model.setRowCount(0);
//Creation du nombre de clonne en fonction du nombre d'attribut
        Object[] colonne = new Object[4];
        for (int i = 0; i < list.size(); i++) {
            colonne[0] = list.get(i).getPrenom();
            colonne[1] = list.get(i).getNom();
            colonne[2] = list.get(i).getSexe();
            colonne[3] = list.get(i).getDomaine();

            model.addRow(colonne);
        }
    }

